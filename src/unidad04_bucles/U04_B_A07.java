package unidad04_bucles;

import java.util.*;

 /*-FOR-Escribir todos los múltiplos de 7 menores que 100.*/
public class U04_B_A07 {
    public static void main (String[]args){
        
        System.out.println("MULTIPLOS DE 7 MENORES QUE 100");
        
        for (int multiplo = 7; multiplo<=100; multiplo+=7){
            System.out.println(multiplo);
        }
    }
}
